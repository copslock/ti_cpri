/** ============================================================================
 *   @n@b gen_ctc_fec_block
 *
 *   @b Description
 *   @n Based on test parameters, generates rest of the code block params for 
 *      the test. 
 * =============================================================================
 */
Void gen_ctc_fec_block (Void)
{
	Int8  j = 0; /* Parameter dependent on the modulation and FEC rate */
	Int16 k = 0; 
	Int16 m = 0;
	Int16 n = 0;
	UInt16 N;
	UInt8 i;

	j = CTC_MCR_TABLE[MCR][2];
	n = numSlotsWoRpt;

	k = n / j;
	m = n % j;

	if( n <= j )
	{
		if ( n != 7)
		{
			fecBlocks.fecCtcBlkLen[0] = n;
			fecBlocks.fecCtcBlkLen[1] = 0;
			fecBlocks.fecCtcBlkLen[2] = 0;
			fecBlocks.numFecBlks1 = 1;
			fecBlocks.numFecBlks2 = 0;
			fecBlocks.numFecBlks3 = 0;

		}
		else if( n == 7 )
		{
			fecBlocks.fecCtcBlkLen[0] = 4;
			fecBlocks.fecCtcBlkLen[1] = 3;
			fecBlocks.fecCtcBlkLen[2] = 0;
			fecBlocks.numFecBlks1 = 1;
			fecBlocks.numFecBlks2 = 1;
			fecBlocks.numFecBlks3 = 0;
		}
	}
	else
	{
		if ( m == 0 )  //n % j
		{
		
			fecBlocks.fecCtcBlkLen[0] = j;
			fecBlocks.fecCtcBlkLen[1] = 0;
			fecBlocks.fecCtcBlkLen[2] = 0;
			
			fecBlocks.numFecBlks1 = k;
			fecBlocks.numFecBlks2 = 0;
			fecBlocks.numFecBlks3 = 0;
		}
		else
		{
			fecBlocks.fecCtcBlkLen[0] = j;
			fecBlocks.fecCtcBlkLen[1] = ( m + j ) / 2 + ( m + j ) % 2;
			fecBlocks.fecCtcBlkLen[2] = ( m + j ) / 2;
			fecBlocks.numFecBlks1 = k-1;
			fecBlocks.numFecBlks2 = 1;
			fecBlocks.numFecBlks3 = 1;
			if ( fecBlocks.fecCtcBlkLen[1] == 7 || fecBlocks.fecCtcBlkLen[2] == 7 )
			{
				fecBlocks.fecCtcBlkLen[1] = fecBlocks.fecCtcBlkLen[1] + 1;
				fecBlocks.fecCtcBlkLen[2] = fecBlocks.fecCtcBlkLen[2] - 1;
			}		
		}
	}
	fecBlocks.numTotalFecBlks = fecBlocks.numFecBlks1 + fecBlocks.numFecBlks2 + fecBlocks.numFecBlks3;


	N = fecBlocks.fecCtcBlkLen[0] * numBitsPerSlot / 2  ; 

	for ( i = 0; i < CTC_MCR_TABLE_NUM; i++ )
	{
		if( modType == gCTCChCodingPerMod[i][0] && codingRate == gCTCChCodingPerMod[i][3] 
				&& N == gCTCChCodingPerMod[i][4])
		{
			fecBlocks.P0[0] = gCTCChCodingPerMod[i][5];
			fecBlocks.P1[0] = gCTCChCodingPerMod[i][6];
			fecBlocks.P2[0] = gCTCChCodingPerMod[i][7];
			fecBlocks.P3[0] = gCTCChCodingPerMod[i][8];
			break;
		}
	}

	for ( i = 0; i < 12; i++ )
	{
		if ( N == subBlockInterleaverTable[i][0] )
		{	
			fecBlocks.M[0] = subBlockInterleaverTable[i][1];
			fecBlocks.J[0] = subBlockInterleaverTable[i][2];
			break;
		}
	}
	

	if (fecBlocks.numFecBlks2)
	{
		N = fecBlocks.fecCtcBlkLen[1] * numBitsPerSlot / 2;

		for ( i = 0; i < CTC_MCR_TABLE_NUM; i++ )
		{
			if( modType == gCTCChCodingPerMod[i][0] && codingRate == gCTCChCodingPerMod[i][3] 
					&& N == gCTCChCodingPerMod[i][4])
			{
				fecBlocks.P0[1] = gCTCChCodingPerMod[i][5];
				fecBlocks.P1[1] = gCTCChCodingPerMod[i][6];
				fecBlocks.P2[1] = gCTCChCodingPerMod[i][7];
				fecBlocks.P3[1] = gCTCChCodingPerMod[i][8];
				break;
			}
		}

		for ( i = 0; i < 12; i++ )
		{	
			if ( N == subBlockInterleaverTable[i][0] )
			{	
				fecBlocks.M[1] = subBlockInterleaverTable[i][1];
				fecBlocks.J[1] = subBlockInterleaverTable[i][2];
				break;
			}
		}
	}

	else
	{
		fecBlocks.P0[1] = 0;
		fecBlocks.P1[1] = 0;
		fecBlocks.P2[1] = 0;
		fecBlocks.P3[1] = 0;
		fecBlocks.M[1] = 0;
		fecBlocks.J[1] = 0;

	}

	if (fecBlocks.numFecBlks3)
	{
		N = fecBlocks.fecCtcBlkLen[2] * numBitsPerSlot / 2;

		for ( i = 0; i < CTC_MCR_TABLE_NUM; i++ )
		{
			if( modType == gCTCChCodingPerMod[i][0] && codingRate == gCTCChCodingPerMod[i][3] 
					&& N == gCTCChCodingPerMod[i][4])
			{
				fecBlocks.P0[2] = gCTCChCodingPerMod[i][5];
				fecBlocks.P1[2] = gCTCChCodingPerMod[i][6];
				fecBlocks.P2[2] = gCTCChCodingPerMod[i][7];
				fecBlocks.P3[2] = gCTCChCodingPerMod[i][8];
				break;
			}
		}

		for ( i = 0; i < 12; i++ )
		{	
			if ( N == subBlockInterleaverTable[i][0] )
			{	
				fecBlocks.M[2] = subBlockInterleaverTable[i][1];
				fecBlocks.J[2] = subBlockInterleaverTable[i][2];
				break;
			}
		}
	}

	else
	{
		fecBlocks.P0[2] = 0;
		fecBlocks.P1[2] = 0;
		fecBlocks.P2[2] = 0;
		fecBlocks.P3[2] = 0;
		fecBlocks.M[2] = 0;
		fecBlocks.J[2] = 0;

	}

    return;
}

/** ============================================================================
 *   @n@b generate_wimax_dl_testvector
 *
 *   @b Description
 *   @n Utility API to generate test parameters. 
 *
 *   @param[in]  
 *   @n maxBurstSize    Burst size
 *
 *   @param[in]  
 *   @n mcrInitVal      MCR Initial value.
 * 
 *   @return        None
 * =============================================================================
 */
Void generate_wimax_dl_testvector (UInt32 maxBurstSize, UInt32 mcrInitVal)
{
	UInt8       numK;
        
    memset (&fecBlocks, 0, sizeof (fecBlocks));
    numSlotsWoRpt = maxBurstSize + 1;

    if (numSlotsWoRpt == 1024) //maximum number of code block is 511
        numK    =   5;
    else
	    numK    =   8;

    /* not used */
    ((Void) (numK));

    MCR         =   mcrInitVal;
    codingRate  =   CTC_MCR_TABLE[MCR][1];
    modType     =   CTC_MCR_TABLE[MCR][0];
			
    // 48 data subcarriers * modualtion * coding rate */
    numBitsPerSlot  =   48 * modType * codingRate/120; //must be multiple of 8
			
    /* calculate the concatenation rule */
    gen_ctc_fec_block();

    totalNumInforBits = numSlotsWoRpt * numBitsPerSlot;
    tbSize = totalNumInforBits - 16 ; //16 bits crc

#ifdef USE_RANDOM_TEST_VECTOR
    rms = SIGRMS[gen_rand()%3];//0,1,2
#else
    rms = 1024;
#endif

    return;
}

